--  DDL for Trigger TRG_ALERT
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "TRG_ALERT" 
BEFORE INSERT OR UPDATE ON AMNIS.ALERT FOR EACH ROW
BEGIN
IF INSERTING THEN
IF 
 :NEW.ALERT_ID IS NULL THEN
  SELECT SEQ_BASE.NEXTVAL 
 INTO :NEW.ALERT_ID FROM DUAL;
END IF;
:NEW.CRD:=SYSTIMESTAMP;		   --LÉTREHOZÁS IDŐPONTJA
:NEW.CRU:=NVL(V('APP_USER'),USER);        --APEX USER AKI LÉTREHOZZA A REKORDOT
ELSIF UPDATING
THEN
  :NEW.MDD:=SYSTIMESTAMP;	     --MÓDOSÍTÁS IDŐPONTJA
  :NEW.MDU:=NVL(V('APP_USER'),USER);       --APEX USER AKI MÓDOSÍT
END IF;
END;
/
ALTER TRIGGER "TRG_ALERT" ENABLE;
